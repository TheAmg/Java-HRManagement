    private void viewbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_viewbMouseClicked
        // TODO add your handling code here:
        String aid=(String) appbox.getSelectedItem();
        try
        {
            Class.forName("com.mysql.jdbc.Driver");  
		Connection con=DriverManager.getConnection(  
		"jdbc:mysql://localhost:3306/hrmgmt","root",""); 
                
                Statement stmt=con.createStatement();
                String sql="select u.username,a.arole,a.deptid,u.qualifications from user u,applications a where a.userid='"+aid+"' and u.userid=a.userid";
                
                ResultSet rs=stmt.executeQuery(sql);
                if(rs.next())
                {
                    namel.setText("Name "+rs.getString(1));
                    String ro=rs.getString(2);
                    String fr="";
                    if(ro.equals("E"))
                    {
                        fr="Employee";
                    }
                    else if(ro.equals("DL"))
                    {
                        fr="Department leader";
                    }
                    roll.setText("Role: "+fr);
                        
                    String deptid=rs.getString(3);
                    deptl.setText("Department id: "+deptid);
                    String qual=rs.getString(4);
                    quall.setText("Qualifications : "+qual);
                }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Error(1) "+e);
        }
    }//GEN-LAST:event_viewbMouseClicked

